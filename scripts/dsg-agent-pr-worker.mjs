#!/usr/bin/env node
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname } from 'node:path';
import { createHash } from 'node:crypto';

const allowedPrefixes = [
  'app/generated-apps/',
  'app/api/generated-apps/',
  'docs/dsg-generated-apps/',
  'docs/dsg-agent-runs/',
  'supabase/migrations/',
];

function sha256(value) {
  return createHash('sha256').update(value).digest('hex');
}

function safeId(value, fallback) {
  return String(value || fallback)
    .toLowerCase()
    .replace(/[^a-z0-9-]+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 60) || fallback;
}

function assertAllowedPath(path) {
  if (!allowedPrefixes.some((prefix) => path.startsWith(prefix))) {
    throw new Error(`DSG_BLOCKED_PATH:${path}`);
  }
}

function writeAllowed(path, content) {
  assertAllowedPath(path);
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, content);
  return { path, bytes: Buffer.byteLength(content) };
}

const goal = process.env.DSG_AGENT_GOAL || 'Generated DSG app';
const jobId = safeId(process.env.DSG_AGENT_JOB_ID, 'agent-worker');
const appId = safeId(process.env.DSG_AGENT_APP_ID || jobId, 'generated-app');
const title = goal.replace(/[`$\\]/g, '').slice(0, 90);
const stamp = new Date().toISOString().replace(/[-:T.Z]/g, '').slice(0, 14);

const page = `export default function GeneratedWorkerAppPage() {
  return (
    <main className="min-h-screen bg-slate-950 px-6 py-10 text-slate-100">
      <section className="mx-auto max-w-4xl rounded-3xl border border-indigo-500/30 bg-indigo-500/10 p-8">
        <p className="text-xs font-bold uppercase tracking-[0.24em] text-indigo-200">DSG Agent Worker App</p>
        <h1 className="mt-4 text-4xl font-black tracking-tight">${title}</h1>
        <p className="mt-4 text-sm leading-7 text-slate-300">
          Generated by DSG CI worker. Production-ready claim remains blocked until CI, deploy, database, and live proof pass.
        </p>
        <div className="mt-6 rounded-2xl border border-amber-400/40 bg-amber-500/10 p-4 text-sm text-amber-100">
          productionReadyClaim: false
        </div>
      </section>
    </main>
  );
}
`;

const api = `import { NextResponse } from 'next/server';

export async function GET() {
  return NextResponse.json({
    ok: true,
    appId: '${appId}',
    productionReadyClaim: false
  });
}
`;

const migration = `-- DSG worker generated migration for ${appId}
create table if not exists public.generated_app_items (
  id uuid primary key default gen_random_uuid(),
  app_id text not null,
  title text not null,
  completed boolean not null default false,
  created_at timestamptz not null default now()
);
create index if not exists generated_app_items_app_id_created_at_idx
  on public.generated_app_items (app_id, created_at desc);
`;

const runbook = `# DSG Agent Worker Run: ${appId}

Goal: ${goal}

## Output

- App route: /generated-apps/${appId}
- API route: /api/generated-apps/${appId}/items

## Truth boundary

- productionReadyClaim: false
- Requires CI/build/deploy/live proof before service claim.

## Required checks

- npm run dsg:typecheck
- npm run build:termux
- open generated app route
- verify API route
`;

const outputs = [
  writeAllowed(`app/generated-apps/${appId}/page.tsx`, page),
  writeAllowed(`app/api/generated-apps/${appId}/items/route.ts`, api),
  writeAllowed(`supabase/migrations/${stamp}_create_generated_app_items_${appId.slice(0, 12)}.sql`, migration),
  writeAllowed(`docs/dsg-generated-apps/${appId}.md`, runbook),
];

const manifest = {
  ok: true,
  jobId,
  appId,
  goal,
  outputs,
  outputHash: sha256(JSON.stringify(outputs)),
  productionReadyClaim: false,
};

writeAllowed(`docs/dsg-agent-runs/${appId}.json`, JSON.stringify(manifest, null, 2));
console.log(JSON.stringify(manifest, null, 2));
